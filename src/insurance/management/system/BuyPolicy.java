    public void policyNameByCategory(String insuranceType) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Insurance_Policy> list = Insurance_Policy_dao.getAllRecordsByInsurance (insuranceType);
        Iterator<Insurance_Policy> itr = list.iterator();
        while(itr.hasNext()) {
            Insurance_Policy insurancePolicyObj = itr.next();
            dtm.addRow(new Object[] {insurancePolicyObj.getName()});
        }
    }
    
    public void filterPolicyByName(String name, String insuranceType) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Insurance_Policy> list = Insurance_Policy_dao.filterInsurancePolicyByName(name,insuranceType);
        Iterator<Insurance_Policy> itr = list.iterator();
        while (itr.hasNext()) {
            Insurance_Policy insurancePolicyObj = itr.next();
            dtm.addRow(new Object[]{insurancePolicyObj.getName()});
        }
    }
    
