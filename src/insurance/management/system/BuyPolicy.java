    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String email = txtEmail.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        String todaydate = dFormat.format(date);
        String total = String.valueOf(totalAmount);
        
        User user = Insurance_AppUser_dao.getUserDetails(userEmail);
        
        Insurance_Policy_Bill insurance_policy_bill = new Insurance_Policy_Bill();
        
        insurance_policy_bill.setId(billId);
        insurance_policy_bill.setCustomerId(user.getId());
        insurance_policy_bill.setBillDate(todaydate);
        insurance_policy_bill.setTotalAmount(total);
        
        Insurance_Policy_Bill_dao.save(insurance_policy_bill); 
     
        for (int i = 0; i < jTable2.getRowCount(); i++) {
            Insurance_Policy_Bill_Details insurance_policy_bill_details = new Insurance_Policy_Bill_Details();
            insurance_policy_bill_details.setBillId(billId);
            Integer Id = Insurance_Policy_dao.getId(jTable2.getValueAt(i, 0).toString());
            insurance_policy_bill_details.setPolicyId(Id);
            Insurance_Policy_Bill_Details_dao.save(insurance_policy_bill_details);
        }

        //Create Document
        String path = "C:\\Users\\nidsp\\OneDrive - Illinois Institute of Technology\\Documents\\Project Stuff\\Path for Saving Document\\Bill Number ";
        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path + "" + billId + ".pdf"));
            doc.open();
            Paragraph insuranceCompanyName = new Paragraph("                                                 Insurance Management Company\n");
            doc.add(insuranceCompanyName);
            Paragraph starLine = new Paragraph("**************************************************************************************************************");
            doc.add(starLine);
            Paragraph paragraph3 = new Paragraph("\tBill ID:"+billId+"\nCustomer Name: "+name+"\nTotal Paid: "+totalAmount);
            doc.add(paragraph3);
            doc.add(starLine);
            PdfPTable tb1 = new PdfPTable(6);
            tb1.addCell("Name");
            tb1.addCell("Insurance Type");
            tb1.addCell("Sum Assured");
            tb1.addCell("Premium");
            tb1.addCell("Tenure");
            tb1.addCell("Description");

            for (int i=0; i<jTable2.getRowCount(); i++) {
                String n0 = jTable2.getValueAt(i, 0).toString();
                String n1 = jTable2.getValueAt(i, 1).toString();
                String n2 = jTable2.getValueAt(i, 2).toString();
                String n3 = jTable2.getValueAt(i, 3).toString();
                String n4 = jTable2.getValueAt(i, 4).toString();
                String n5 = jTable2.getValueAt(i, 5).toString();
                tb1.addCell(n0);
                tb1.addCell(n1);
                tb1.addCell(n2);
                tb1.addCell(n3);
                tb1.addCell(n4);
                tb1.addCell(n5);
            }
            
            doc.add(tb1);
            doc.add(starLine);
            Paragraph thanksMsg = new Paragraph("Thank You");
            doc.add(thanksMsg);
            
            //Open PDF
            OpenPDF.openById(String.valueOf(billId));
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
        doc.close();
        setVisible(false);
        new Home(email).setVisible(true);
    }//GEN-LAST:event_btnGenerateBillActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int index = jTable2.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this policy?","Select",JOptionPane.YES_NO_OPTION);
        if(a==0) {
            TableModel model = jTable2.getModel();
            String total = model.getValueAt(index, 3).toString();
            totalAmount = totalAmount - Integer.parseInt(total);
            lblTotalAmount.setText(String.valueOf(totalAmount));
            ((DefaultTableModel) jTable2.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_jTable2MouseClicked

