    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<Insurance> list = Insurance_dao.getAllRecords();
        Iterator<Insurance> itr = list.iterator();
        while(itr.hasNext()) {
            Insurance insuranceObj = itr.next();
            txtInsuranceType.addItem(insuranceObj.getName());
        }
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Insurance insurance = Insurance_dao.getId((String)txtInsuranceType.getSelectedItem());
        Insurance_Policy insurancePolicy = new Insurance_Policy();
        insurancePolicy.setName(txtName.getText());
        insurancePolicy.setInsuranceId(insurance.getId());
        insurancePolicy.setSumAssured(txtSumAssured.getText());
        insurancePolicy.setPremium(txtPremium.getText());
        insurancePolicy.setTenure(txtTenure.getText());
        insurancePolicy.setDescriptions(txtDescriptions.getText());
        Insurance_Policy_dao.save(insurancePolicy);
        setVisible(false);
        new NewPolicy().setVisible(true);
    }//GEN-LAST:event_btnSaveActionPerformed

