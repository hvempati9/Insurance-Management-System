    public void validateFields () {
        String name = txtName.getText();
        String sumAssured = txtSumAssured.getText();
        String premium = txtPremium.getText();
        String tenure = txtTenure.getText();
        String descriptions = txtDescriptions.getText();
        
        if(!name.equals("") &&
           !sumAssured.equals("") &&
           !premium.equals("") &&
           !tenure.equals("") &&
           !descriptions.equals("")) {
            btnSave.setEnabled(true);
        }
        else {
            btnSave.setEnabled(false);
        }
        
    }

