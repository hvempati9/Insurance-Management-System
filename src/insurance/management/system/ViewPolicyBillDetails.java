    public void tableDetails() {
        String date = txtFilter.getText();
        String incDesc = (String) changeOrderComboBox.getSelectedItem();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        if(incDesc.equals("INC")) {
            ArrayList<Insurance_Policy_Customer_Bill> list = Insurance_Policy_Customer_Bill_dao.getAllRecordsByInc(date,userEmail);
            Iterator<Insurance_Policy_Customer_Bill> itr = list.iterator();
            while(itr.hasNext()) {
                Insurance_Policy_Customer_Bill insurancePolicyCustomerBillObj = itr.next();
                dtm.addRow(new Object[] {insurancePolicyCustomerBillObj.getId(),insurancePolicyCustomerBillObj.getName(),
                insurancePolicyCustomerBillObj.getMobileNumber(),insurancePolicyCustomerBillObj.getEmail(),
                insurancePolicyCustomerBillObj.getBillDate(),insurancePolicyCustomerBillObj.getTotalAmount()});
            }
        }
        else {
            ArrayList<Insurance_Policy_Customer_Bill> list = Insurance_Policy_Customer_Bill_dao.getAllRecordsByDesc(date,userEmail);
            Iterator<Insurance_Policy_Customer_Bill> itr = list.iterator();
            while(itr.hasNext()) {
                Insurance_Policy_Customer_Bill insurancePolicyCustomerBillObj = itr.next();
                dtm.addRow(new Object[] {insurancePolicyCustomerBillObj.getId(),insurancePolicyCustomerBillObj.getName(),
                insurancePolicyCustomerBillObj.getMobileNumber(),insurancePolicyCustomerBillObj.getEmail(),
                insurancePolicyCustomerBillObj.getBillDate(),insurancePolicyCustomerBillObj.getTotalAmount()});
            } 
        }
    }

