    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0);
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        Integer BillId = (Integer) model.getValueAt(index, 0);
        ArrayList<Insurance_Policy_List> list = Insurance_Policy_List_dao.getAllRecordsByBillId(BillId);
        Iterator<Insurance_Policy_List> itr = list.iterator();
        while (itr.hasNext()) {
            Insurance_Policy_List insurancePolicyListObj = itr.next();
            dtm.addRow(new Object[]{insurancePolicyListObj.getName(),
                insurancePolicyListObj.getInsuranceType(), insurancePolicyListObj.getSumAssured(),
                insurancePolicyListObj.getPremium(), insurancePolicyListObj.getTenure(),
                insurancePolicyListObj.getDescriptions()});
        }
    }//GEN-LAST:event_jTable1MouseClicked

